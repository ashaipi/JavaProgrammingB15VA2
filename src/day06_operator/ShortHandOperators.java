package day06_operator;

public class ShortHandOperators {
    public static void main(String[] args) {
        int cars = 15;
        System.out.println("Cars in parking lot: "+cars);
        cars = cars + 2;
        System.out.println("Cars in parking lot: "+cars);
        cars +=3;
        System.out.println("Cars in parking lot: "+cars);
        cars = cars -1;
        System.out.println("Cars in parking lot: "+cars);
        cars -=2;
        System.out.println("Cars in parking lot: "+cars);
        cars *= 3;
        System.out.println("Cars in parking lot: "+cars);
        cars /= 3;
        System.out.println("Cars in parking lot: "+cars);
        String a="Abub";
        a+="aker";
        System.out.println(a);

    }
}
